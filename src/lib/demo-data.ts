// ---------------------------------------------------------------------------
// Date helper: generates ISO date strings relative to "today" so demo data
// always feels current regardless of when the app is opened.
// ---------------------------------------------------------------------------
function daysFromNow(offset: number): string {
  const d = new Date();
  d.setDate(d.getDate() + offset);
  d.setHours(0, 0, 0, 0);
  return d.toISOString();
}

