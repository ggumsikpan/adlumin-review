// ---------------------------------------------------------------------------
// Helper: check if currently in demo mode (client-side)
// ---------------------------------------------------------------------------
export function isDemoMode(): false | "advertiser" | "influencer" {
  if (typeof document === "undefined") return false;
  const match = document.cookie.match(/(?:^|; )adlumin_demo=([^;]*)/);
  if (!match) return false;
  const value = match[1];
  if (value === "advertiser" || value === "influencer") return value;
  return false;
}

// ---------------------------------------------------------------------------
// Helper: clear demo mode
// ---------------------------------------------------------------------------
export function clearDemoMode() {
  document.cookie = "adlumin_demo=; path=/; max-age=0";
}

// ---------------------------------------------------------------------------
// Helper: toggle demo mode to a different role (calls API, returns a promise)
// ---------------------------------------------------------------------------
export async function toggleDemoMode(
  role: "advertiser" | "influencer",
): Promise<boolean> {
  try {
    const res = await fetch("/api/demo-login", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ role }),
    });
    return res.ok;
  } catch {
    return false;
  }
}
